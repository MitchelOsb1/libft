#include "libft.h"

int	ft_iswhitespace(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n') ||
			(str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
			return (1);
	}
	return (0);
}
